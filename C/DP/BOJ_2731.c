#include <stdio.h>
int d[1003];

int main()
{
    int num;
    scanf("%d",&num);
    d[1] = 1;
    d[2] = 3;
    for(int i = 3; i <= num; i++){
        d[i] = (d[i-1] + d[i-2]*2)%10007;
    }
    printf("%d", d[num]);
    return 0;
}

